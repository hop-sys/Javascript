// string data type
// A string is a series/sequence of characters that are enclosed in qoutes.
let name = "Hope"
console.log("My name is: ", name)
console.log(typeof(name))

// flaoting point: This is a number with a decimal
let quantity = 35.5
console.log("The quantity of sugar bought is: ", quantity)
console.log(typeof(quantity))

// Boolean : It returns true or false
let isAdult = true
let isRegistered = false
console.log("Is James an adult? ", isAdult)
console.log("Is James registered as a voter? ", isRegistered)
console.log(typeof(isAdult))

// undefined : This is a datatype that has been declared but there is no value that has been assigned to it.
let county;
console.log("The county is: ", county)
console.log(typeof(county))

// null : It is a data type that contains null values.
let username=null
console.log("What is the username? ", username)
console.log(typeof(username))

