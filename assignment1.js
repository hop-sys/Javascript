// Array : An array is a data structure that can hold multiple values of different data types.
// Arrays use index numbers (starting from 0).
let fruits = ["Apple", "Mango", "Banana"];
console.log(fruits[0])  
console.log(fruits[1])  
console.log(fruits[2])  
console.log(typeof fruits)

// Object : An object is a collection of properties, where each property is a key-value pair.
let student = {
    name: "Hope",
    age: 16,
    isActive: true
}
console.log(student.name)
console.log(student.age)    